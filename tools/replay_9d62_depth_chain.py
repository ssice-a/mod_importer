"""
Replay the early position-building portion of the 9d62 depth VS offline.

Current scope:
- Decode the fixed section/basis selection for a dumped draw.
- Export the raw custom `vb0` mesh.
- Export the "basis-applied before t3/t5/t6" mesh:
    position = offset + vb0.x * basis_x + vb0.y * basis_y + vb0.z * basis_z

This intentionally stops before the later t3/t5/t6 correction chain. The goal is
to answer a narrow question first: does the mesh already become wrong when 9d62
applies the section basis to our local skinned position buffer?

Usage:
    python tools/replay_9d62_depth_chain.py

Outputs:
    <FRAME_DIR>/replay_9d62_000148/
      - 000148_vb0_raw.obj
      - 000148_basis_applied.obj
      - 000148_report.json
"""

